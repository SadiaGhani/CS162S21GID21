    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      
        
          String v1 = Vname.getText();
          String v2 = Vcnic.getText();
          String v3 = Vnum.getText();
          String v4 = Vrel.getText();
          boolean b1,b2,b3,b4;
          b1 = v.setVName(v1);
          b2 = v.setVCnic(v2);
          b3 = v.setVContact(v3);
          b4 = v.setVRelation(v4);
          v.setVId();
          if(b1==true&&b2==true&&b3==true&&b4==true)
          {
             // list.add(v);
              FTProject.getInstance().getVList().add(v);
               
               
               FTProject.getInstance().writeDataVisitors();
                JOptionPane.showMessageDialog(null, " Data added successfully on the ID "+"\n"+v.getVId());
          }
          else
          {
              JOptionPane.showMessageDialog(null, " Invalid data entry!!");
          }

        
        
        
        
        
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

