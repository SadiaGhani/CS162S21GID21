    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String q1 = pname.getText();
       String q2 = page.getText();
       String q3 = proom.getText();
       if(q1.equals(null))
       {
           FTProject.getInstance().getPriList().get(index).setName(FTProject.getInstance().getPriList().get(index).getPName());
       }
       else
       {
           FTProject.getInstance().getPriList().get(index).setName(q1);
       }
       if(q2.equals(null))
       {
           FTProject.getInstance().getPriList().get(index).setPAge(FTProject.getInstance().getPriList().get(index).getPAge());
       }
       else
       {
           FTProject.getInstance().getPriList().get(index).setPAge(Integer.parseInt(q2));
       }
       if(q3.equals(null))
       {
           FTProject.getInstance().getPriList().get(index).setRoom(FTProject.getInstance().getPriList().get(index).getPRoom());
       }
       else
       {
           FTProject.getInstance().getPriList().get(index).setRoom(Integer.parseInt(q3));
       }
       FTProject.getInstance().writeDataPrisoners();
       addDataToRow();
       JOptionPane.showMessageDialog(null, " Data Update Successfully");
    }//GEN-LAST:event_jButton2ActionPerformed

