    public void addDataToRow()
    {
        
        DefaultTableModel table = (DefaultTableModel) ptable.getModel();
        Object Items[] = new Object[4];
        table.setRowCount(0);
        for(int i=0; i<FTProject.getInstance().getPriList().size(); i++)
        {
            Items[0] = FTProject.getInstance().getPriList().get(i).getPId();
            Items[1] = FTProject.getInstance().getPriList().get(i).getPName();
            Items[2] = FTProject.getInstance().getPriList().get(i).getPAge();
            Items[3] = FTProject.getInstance().getPriList().get(i).getPRoom();
           
            table.addRow(Items);
        }
        
    }


