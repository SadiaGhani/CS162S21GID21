    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String id = pid.getText();
        for(int i=0; i<FTProject.getInstance().getPriList().size(); i++)
        {
            if(id.equals(FTProject.getInstance().getPriList().get(i).getPId()))
            {
                index = i;
                break;
            }
            else
                index = -1;
        }
        if(index == -1)
        {
            JOptionPane.showMessageDialog(null,"The ID not Found");
        }
        else
        {
            pname.setText(FTProject.getInstance().getPriList().get(index).getPName());
            page.setText(FTProject.getInstance().getPriList().get(index).getPAge()+"");
            proom.setText(FTProject.getInstance().getPriList().get(index).getPRoom()+"");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

